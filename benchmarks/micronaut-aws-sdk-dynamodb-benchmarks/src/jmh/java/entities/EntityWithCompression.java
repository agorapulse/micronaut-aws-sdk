package entities;

import com.agorapulse.micronaut.aws.dynamodb.convert.CompressedStringConverter;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTypeConverted;
import io.micronaut.core.annotation.Introspected;

@Introspected
@DynamoDBTable(tableName = "EntityWithCompression")
public final class EntityWithCompression {

    @DynamoDBHashKey
    private String id;
    @DynamoDBTypeConverted(converter = CompressedStringConverter.class)
    private String data;

    public EntityWithCompression() {
    }

    public EntityWithCompression(String id, String data) {
        this.id = id;
        this.data = data;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
